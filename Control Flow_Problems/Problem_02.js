// Javascript Program to Check if a number is Positive, Negative, or Zero

let num = prompt("Enter a number");

if (num > 0) {
  console.log(`Hey, ${num} is a positive number`);
} else if (num == 0) {
  console.log(`Hey, ${num} is a zero`);
} else {
  console.log(`Hey, ${num} is negative number`);
}
